const y = 75;

console.log(`Constante x = ${x}.`);
console.log(`Constante y = ${y}.`);

if (x > y) {
    console.log(`A constante x é maior que a constante y = ${x} > ${y}!`);
} else {
    console.log(`A constante y é maior que a constante x = ${y} > ${x}!`);
}